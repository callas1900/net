def main():
    raw_path = "raw_frequency.csv"
    output_path = "vocabulary.csv"

    with open(raw_path, encoding="utf-8-sig") as f:
        rows = list(csv.DictReader(f))

    output_rows = []
    missing = []

    for row in rows:
        lemma = row["lemma"]
        count = int(row["count"])
        locations = row["locations"]
        pos_estimate = row["pos_estimate"]

        if lemma in TRANSLATIONS:
            jp, pos = TRANSLATIONS[lemma]
        else:
            jp = "（要翻訳）"
            pos = pos_estimate
            missing.append(lemma)

        category, related, in_dict = EXTENDED_FIELDS.get(lemma, ("", "", "×"))

        output_rows.append({
            "英語名":     lemma,
            "日本語訳":   jp,
            "品詞":       pos,
            "対象分類":   category,
            "関連語":     related,
            "独自辞書":   in_dict,
            "場所":       locations,
            "出現数":     count,
        })

    with open(output_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["英語名", "日本語訳", "品詞", "対象分類", "関連語", "独自辞書", "場所", "出現数"],
        )
        writer.writeheader()
        writer.writerows(output_rows)

    print(f"出力: {output_path}")
    print(f"総語彙数: {len(output_rows)}")
    if missing:
        print(f"\n未翻訳語 ({len(missing)} 語):")
        for w in missing:
            print(f"  {w}")
    else:
        print("未翻訳語: なし")


if __name__ == "__main__":
    main()
